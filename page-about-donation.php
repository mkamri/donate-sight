            <h3 class="blue mb-3">What does an Eye Bank do?</h3>
            <p>An eye bank is a medical facility where technicians surgically recover deceased ocular tissue from consented donors and then evaluate it with technology specific to ophthalmology. Donor suitability is then determined based on medical/social history, serological/other laboratory testing, and final tissue suitability. Finally, the cornea is processed to surgeon specifications, and then distributed for transplant locally, nationally, and internationally.</p>
